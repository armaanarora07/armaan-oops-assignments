        System.out.println("\n******************************************");
        
        // Printing the grades table header
        System.out.println("Subject\t\tMarks\t\tGrade");
        System.out.println("******************************************");
        
        // Printing subject-wise marks and grades
        System.out.println("Maths\t\t88\t\tB+");
